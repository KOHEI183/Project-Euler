# 左右どちらから読んでも同じ値になる数を回文数という. 2桁の数の積で表される回文数のうち, 最大のものは 9009 = 91 × 99 である.
# では, 3桁の数の積で表される回文数の最大値を求めよ.

# 1.1000×9999範囲を抽出
# 2.全部かける?
# 3.回文数を出す
# 4.max抽出

a = (1000..9999)

































n=(1000..9999).flat_map{|x| (1000..9999).map{|y| x*y}}.select{|xy| (s=xy.to_s)==s.reverse}.max
